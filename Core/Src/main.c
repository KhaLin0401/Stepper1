void StartUartTask(void *argument)
{
  /* USER CODE BEGIN StartUartTask */
  /* Infinite loop */
  for(;;)
  {
    // Update Modbus counter
    g_modbusCounter++;
    
    // Check for UART timeout (10 seconds)
    if (HAL_GetTick() - g_lastUARTActivity > 10000) {
      resetUARTCommunication();
      g_lastUARTActivity = HAL_GetTick();
    }
    
    // Process Modbus frame if received
    if (frameReceived) {
      processModbusFrame();
    }
    
    osDelay(100); // 100ms delay
  }
  /* USER CODE END StartUartTask */
}

/* USER CODE BEGIN Header_MotorTaskStart */
/**
* @brief Function implementing the MotorTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_MotorTaskStart */
void MotorTaskStart(void *argument)
{
  const uint16_t M1_BASE_ADDR = 0x0010;
  const uint16_t M2_BASE_ADDR = 0x0020;
  
  // MODIFICATION LOG
  // Date: 2025-01-26
  // Changed by: AI Agent
  // Description: Added PID initialization and proper motor task sequence
  // Reason: PID state was not initialized causing incorrect calculations
  // Impact: PID controllers now work properly from startup
  // Testing: Test PID response after system reset
  
  // Initialize PID controllers with default values
  PID_Init(1, DEFAULT_PID_KP, DEFAULT_PID_KI, DEFAULT_PID_KD); // Motor 1
  PID_Init(2, DEFAULT_PID_KP, DEFAULT_PID_KI, DEFAULT_PID_KD); // Motor 2

  // Vòng lặp RTOS
  for (;;)
  {
      // 1. Load dữ liệu từ Modbus registers
      MotorRegisters_Load(&motor1, M1_BASE_ADDR);
      MotorRegisters_Load(&motor2, M2_BASE_ADDR);

      // 2. Xử lý logic điều khiển motor 1
      Motor_ProcessControl(&motor1);

      // 3. Xử lý logic điều khiển motor 2
      Motor_ProcessControl(&motor2);

      // 4. Save lại dữ liệu ngược ra Modbus registers
      MotorRegisters_Save(&motor1, M1_BASE_ADDR);
      MotorRegisters_Save(&motor2, M2_BASE_ADDR);

      // 5. Delay theo chu kỳ task (ví dụ 10ms)
      osDelay(10);
  }
}

